package com.ukg.entity;

public class DataPlot {

	private String duration;
	
	private Long scale;

	public String getDuration() {
		return duration;
	}

	public DataPlot setDuration(String duration) {
		this.duration = duration;
		return this;
	}

	public Long getScale() {
		return scale;
	}

	public DataPlot setScale(Long scale) {
		this.scale = scale;
		return this;
	}
	
	
}
